package com.whizzosoftware.hobson.api.config;

/**
 * Represents a configuration property enumerated value.
 *
 * @author Dan Noguerol
 */
public class ConfigurationEnumValue {
    private String id;
    private String name;

    public ConfigurationEnumValue(String id, String name) {
        this.id = id;
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }
}
